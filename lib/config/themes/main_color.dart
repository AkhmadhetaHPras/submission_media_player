import 'dart:ui';

/// An abstract class that provides constants for various colors that may be used in the layout and appearance of the application.
/// Each color constant has a hexadecimal value corresponding to the desired color.
abstract class MainColor {
  /// [white]
  static const Color whiteFFFFFF = Color(0xFFFFFFFF);
  static const Color whiteF2F0EB = Color(0xFFF2F0EB);

  /// [grey]
  static const Color greyD4D2CE = Color(0xFFD4D2CE);
  static const Color greyB6B5B1 = Color(0xFFB6B5B1);
  static const Color grey989794 = Color(0xFF989794);

  /// [black]
  static const Color black000000 = Color(0xFF000000);
  static const Color black222222 = Color(0xFF222222);
  static const Color black120911 = Color(0x7F120911);
  static const Color black0D0D0D = Color(0xFF0D0D0D);

  /// [red]
  static const Color redDC3545 = Color(0xFFDC3545);

  /// [purple]
  static const Color purple5A579C = Color(0xFF5A579C);
}
